var input = Number(prompt("Please Enter Some Number between 1 to 5"));

switch(input) {
    case 1: 
        alert("Your Number is 1");
        break;
    case 2:
        alert("Your Number is 2");
        break;
    case 3:
        alert("Your Number is 3");
        break;
    case 4:
        alert("Your Number is 4");
        break;
    case 5:
        alert("Your Number is 5");
        break;
    default: 
    alert("Not a valid entry, Please enter a number between 1 to 5");
    break;
}
