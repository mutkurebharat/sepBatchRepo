var input = Number(prompt("Please Enter Some Number between 1 to 5"));

if(input === 1) {
    alert("You have entered 1");
} else if(input === 2) {
    alert("You have entered 2");
} else if(input === 3) {
    alert("You have entered 3");
} else if(input === 4) {
    alert("You have entered 4");
} else if(input === 5) {
    alert("You have entered 5");
} else if(input != 1 && input != 2 && input != 3 && input != 4 && input != 5) {
    alert("Not a valid entry, Please enter a number between 1 to 5")
}



